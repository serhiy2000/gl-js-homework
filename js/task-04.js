console.log('==== task 04 ====');
let credits = 23580;
let pricePerDroid = 3000;
let totalPrice;
let droids = prompt('How many droids you would like to buy?');
if (droids === null) {
	alert('Cancelled by user');
} else {
	totalPrice = droids * pricePerDroid;
	if (totalPrice > credits) {
		alert('Not enought credits on your account.');
	} else {
		credits = credits - totalPrice;
		alert('You have bought ' + droids + ' droids. Balance is ' + credits);
	}
}
